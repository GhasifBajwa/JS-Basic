//basic operations
console.log(2 > 1);
console.log(2 < 1);
console.log(2 == 1);
console.log(2 != 1);
console.log(2 >= 1);
console.log(2 <= 1);
console.log(2 === 1);
console.log(2 !== 1);
// basic operations with string or other data types
console.log("2" > 1);
console.log("02" > 1);
// null and undefined give diiferent results as comapared to expectation
console.log(null > 0);
console.log(null == 0);
console.log(null >= 0);
console.log(null <= 0);
console.log(undefined > 0);
console.log(undefined == 0);
console.log(undefined >= 0);
console.log(undefined <= 0);
// === strict equality operator
console.log(2 === "2");
